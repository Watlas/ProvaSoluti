    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        try {
            //valida√ßoes de campos vazios
            float resultado;
            String situacao;
            if(jTextFieldPeso.getText().isEmpty() == true){
               throw new Exception("Preencha o peso");           
            }
            if(jtextFieldAltura.getText().isEmpty() == true){
                 throw new Exception("Preencha a altura");
            }
            
            
            objeto = new Imc();
            objeto.setPeso(Float.parseFloat(jTextFieldPeso.getText().replace(",", ".")));
            objeto.setAltura(Float.parseFloat(jtextFieldAltura.getText()));
           
            resultado = Float.parseFloat(df.format(objeto.calcularImc(objeto.getPeso(), objeto.getAltura())).toString().replace(",", "."));
            if(resultado < 18.5){
                situacao = "Abaixo do peso";
            }else if(resultado >= 18.5 && resultado <= 25){
                 situacao = "Peso normal";
            }else if(resultado >25 && resultado <=30){
                situacao = "Acima do peso";
            }else{
                situacao = "Obesidade";
            }
            //SAIDA
            String saida = "SEU IMC: \n"
                    + resultado +"\n"
                    + "SITUACAO:"+ situacao;
            jTextPaneSaida.setText(saida);
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButtonCalcularActionPerformed

